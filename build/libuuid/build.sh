# Load support functions
. ../../lib/functions.sh

PROG=libuuid
VER=1.0.3
PKG=library/libuuid
SUMMARY="The libuuid library is used to generate unique identifiers for objects that may be accessible beyond the local system."
DESC="The libuuid library is used to generate unique identifiers for objects that may be accessible beyond the local system. The Linux implementation was created to uniquely identify ext2 filesystems created by a machine. This library generates UUIDs compatible with those created by the Open Software Foundation (OSF) Distributed Computing Environment (DCE) utility uuidgen. The UUIDs generated by this library can be reasonably expected to be unique within a system, and unique across all systems. They could be used, for instance, to generate unique HTTP cookies across multiple web servers without communication between the servers, and without fear of a name clash."

CPPFLAGS="-D_REENTRANT"

init
download_source $PROG $PROG $VER
patch_source
prep_build
build
make_isa_stub
make_package
